from django.core.validators import RegexValidator

"""
Валидатор телефонного номера.
Проверяет, что значение состоит из 5–15 цифр,
возможно с префиксом '+' в начале.

Примеры допустимых номеров:
- 24901
- +71112223344
- 81112223344
"""
phone_validator = RegexValidator(
    regex=r"^\+?\d{5,15}$",
    message="""Номер телефона должен быть в формате:
             '24901 / +71112223344 / 81112223344'"""
)
